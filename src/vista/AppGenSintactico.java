    private void llenarFormSelected() {
        TFSimb.setText("" + Seleccionado.getSimbolo());
        if (Seleccionado.getApDer() == null) {
            TFApDer.setText("null");
        } else {
            TFApDer.setText(Seleccionado.getApDer().toString());
        }

        if (Seleccionado.getApAbajo() == null) {
            TFApAbajo.setText("null");
        } else {
            TFApAbajo.setText(Seleccionado.getApAbajo().toString());
        }
    }

