    private void llenarList() {
        DefaultListModel<Nodo> modelo = new DefaultListModel<>();
        listaNodos.stream().forEach((listaNodo) -> {
            modelo.addElement(listaNodo);
        });
        jList1.setModel(modelo);
    }

