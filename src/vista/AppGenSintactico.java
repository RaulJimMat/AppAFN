    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String simbolo = JOptionPane.showInputDialog("Simbolo para el nodo").trim();
        if ("eps".equals(simbolo)) {
            Nodo nuevo = new Nodo((char)0);
            listaNodos.add(nuevo);
            llenarList();
        } else {
            Nodo nuevo = new Nodo(simbolo.charAt(0));
            listaNodos.add(nuevo);
            llenarList();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        JList ls = (JList) evt.getSource();
        if (!(ls.isSelectionEmpty())) {
            Seleccionado = (Nodo) ls.getSelectedValue();
            llenarFormSelected();
        }
    }//GEN-LAST:event_jList1ValueChanged

    private void BtnSetApDerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSetApDerActionPerformed
        // TODO add your handling code here:
        Object[] arrlist = new Object[listaNodos.size()];
        for (int i = 0; i < listaNodos.size(); i++) {
            arrlist[i] = listaNodos.get(i);
        }
        Nodo apder = (Nodo) JOptionPane.showInputDialog(this, "Selecciona el apuntador Derecho", "Apuntador Derecho", JOptionPane.PLAIN_MESSAGE, frameIcon, arrlist, arrlist[0]);
        Seleccionado.setApDer(apder);
        llenarList();
        llenarFormSelected();
    }//GEN-LAST:event_BtnSetApDerActionPerformed

